/// The input given to a bytecode-loadable machine.
#[derive(Debug)]
pub struct BytecodeLoadInput<'bc, C = ProgramContext> {
    pub section: BytecodeSectionView<'bc, C>,
}

impl<'bc, C> Clone for BytecodeLoadInput<'bc, C> {
    fn clone(&self) -> Self {
        Self {
            section: self.section.clone(),
        }
    }
}

impl<'bc, C> From<&'bc BytecodeFile<C>> for BytecodeLoadInput<'bc, C> {
    fn from(file: &'bc BytecodeFile<C>) -> Self {
        Self {
            section: file.root(),
        }
    }
}

impl<'bc, C> From<BytecodeSectionView<'bc, C>> for BytecodeLoadInput<'bc, C> {
    fn from(section: BytecodeSectionView<'bc, C>) -> Self {
        Self { section }
    }
}

